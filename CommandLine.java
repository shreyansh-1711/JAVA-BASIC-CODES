//Command Line (as name implies, works on CMD)
//takes input of 4 integers and prints them

public class CommandLine {
    public static void main(String arg[]) {
        System.out.println("Your argument 1 is: " + arg[0]);
        System.out.println("Your argument 2 is: " + arg[1]);
        System.out.println("Your argument 3 is: " + arg[2]);
        System.out.println("Your argument 4 is: " + arg[3]);

    }
}